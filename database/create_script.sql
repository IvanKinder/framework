DROP TABLE IF EXISTS students;
CREATE TABLE students (id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL UNIQUE, username VARCHAR (32) UNIQUE);
DROP TABLE IF EXISTS categories;
CREATE TABLE categories (id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL UNIQUE, name VARCHAR (32) UNIQUE);
DROP TABLE IF EXISTS courses;
CREATE TABLE courses (id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL UNIQUE, name VARCHAR (32) UNIQUE);
DROP TABLE IF EXISTS student_course;
CREATE TABLE student_course (id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL UNIQUE, student INTEGER, course INTEGER, UNIQUE (student, course));
DROP TABLE IF EXISTS category_course;
CREATE TABLE category_course (id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL UNIQUE, category INTEGER, course INTEGER, UNIQUE (category, course));
